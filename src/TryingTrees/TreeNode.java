package TryingTrees;

public class TreeNode {

    int val;              // значение узла
    TreeNode left;        // ссылка на левого потомка
    TreeNode right;       // ссылка на правого потомка

    public TreeNode(int val) {
        this.val = val;
        this.left = null;
        this.right = null;
    }
}
